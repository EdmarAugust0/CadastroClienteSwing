    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        int linhaSelecionada = tabelaClientes.getSelectedRow();
        
        
        if(linhaSelecionada >= 0) {
           Long cpf = (Long) tabelaClientes.getValueAt(linhaSelecionada, 1);
        
           Cliente cliente = this.clienteDAO.consultar(cpf); 
           
            if(cliente != null){
             cliente.setNome(txtNome.getText());
             cliente.setCpf(Long.parseLong(txtCPF.getText()));
             cliente.setTel(Long.parseLong(txtTel.getText()));
             cliente.setEnd(txtEnd.getText());
             cliente.setNumero(Integer.parseInt(txtNumber.getText()));
             cliente.setCidade(txtCity.getText());
             cliente.setEstado(txtState.getText());

             this.clienteDAO.alterar(cliente);

             tabelaClientes.setValueAt(cliente.getNome(), linhaSelecionada, 0);
             tabelaClientes.setValueAt(cliente.getCpf(), linhaSelecionada, 1);
             tabelaClientes.setValueAt(cliente.getTel(), linhaSelecionada, 2);
             tabelaClientes.setValueAt(cliente.getEnd(), linhaSelecionada, 3);
             tabelaClientes.setValueAt(cliente.getNumero(), linhaSelecionada, 4);
             tabelaClientes.setValueAt(cliente.getCidade(), linhaSelecionada, 5);
             tabelaClientes.setValueAt(cliente.getEstado(), linhaSelecionada, 6);
            
                JOptionPane.showMessageDialog(null, "Cliente alterado com sucesso", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
                limparCampos();
            }   
        }else {
            JOptionPane.showMessageDialog(null, "Nenhum cliente selecionado.", "Erro", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnAlterarActionPerformed

